        <h3>The World's Leading Exploration and Production Publication</h3>
        <p><em>E&amp;P</em> is the world's leading publication dedicated to
        informing exploration, drilling and production operations managers,
        onshore and offshore and in all parts of the world, about the trends
        and technology they need to do their jobs more efficiently. Experienced
        editors and industry experts offer the most accurate, most up-to-date
        information available to guide operations managers through exploration
        technology that helps them find more resources; drilling technology
        that gives them better, less-expensive wells, and production technology
        that keeps fields producing more oil and gas for longer periods of
        time. The quality and breadth of coverage has earned awards for
        editorial excellence and quality content.</p>
